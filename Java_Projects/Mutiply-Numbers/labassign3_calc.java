public class labassign3_calc {

    public static void main(String[] args) {
        System.out.println("From 1 - 5 is like");
        for (int i=1; i<=5; i++) {
            for (int j=1; j<=5; j++) {
                System.out.print(i+" x "+j+" = "+i*j+"     ");
            }
            System.out.println();
        }
    }
    
}
